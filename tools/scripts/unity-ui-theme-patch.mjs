#!/usr/bin/env node
/**
 * unity-ui-theme-patch — Phase A2 (UI Toolkit Parity Recovery, docs/ui-toolkit-parity-recovery-plan.html).
 *
 * Reads `Assets/UI/Snapshots/panels.json` (panel rows). For each item with a
 * `fields.theme` value (default "dark"), patches the corresponding
 * `Assets/UI/Generated/{slug}.uxml` and `Assets/UI/Generated/{slug}.uss` so
 * the `<Style src="project://database/Assets/UI/Themes/{theme}.tss"/>` line
 * and `@import url("project://database/Assets/UI/Themes/{theme}.tss")` line
 * reflect the panel's chosen theme.
 *
 * Idempotent: no-op when the file already matches panel.theme. Adds a
 * `<!-- Theme patched by unity-ui-theme-patch — panel.theme={theme} -->`
 * marker comment under the existing "Generated by …" comment so callers can
 * tell a theme-patched file from a freshly hand-authored one.
 *
 * Why a script and not a bake-handler patch:
 *   The current bake pipeline (`UiBakeHandler.BakeFromPanelSnapshot`) emits
 *   prefabs only; the UXML/USS files in `Assets/UI/Generated/` are committed
 *   source (rich, hand-authored prior to DB-primary). `UxmlEmissionService`
 *   exists but is dead code (the simple `BuildUxml` template would destroy
 *   the rich content). Phase A2 closes the theme-dispatch gap without
 *   regressing the rich UXMLs; full DB-canonical emission is a follow-up
 *   (see TODO Phase A1 followup in cream.tss + DEC-A28 §I1).
 *
 * Usage:
 *   node tools/scripts/unity-ui-theme-patch.mjs                # patch all panels
 *   node tools/scripts/unity-ui-theme-patch.mjs --dry-run      # report only
 *   node tools/scripts/unity-ui-theme-patch.mjs --slugs A,B    # filter
 */

import * as fs from 'node:fs';
import * as path from 'node:path';

const REPO_ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..', '..');
const PANELS_JSON = path.join(REPO_ROOT, 'Assets', 'UI', 'Snapshots', 'panels.json');
const GENERATED_DIR = path.join(REPO_ROOT, 'Assets', 'UI', 'Generated');

const argv = process.argv.slice(2);
const dryRun = argv.includes('--dry-run');
const slugsArgIdx = argv.findIndex((a) => a === '--slugs');
const slugFilter = slugsArgIdx !== -1 && argv[slugsArgIdx + 1]
  ? new Set(argv[slugsArgIdx + 1].split(',').map((s) => s.trim()).filter(Boolean))
  : null;

function panelsItems() {
  const raw = JSON.parse(fs.readFileSync(PANELS_JSON, 'utf8'));
  return raw.items ?? [];
}

const STYLE_SRC_RE = /<Style src="project:\/\/database\/Assets\/UI\/Themes\/([a-zA-Z0-9_\-]+)\.tss"\/>/;
const IMPORT_URL_RE = /@import url\("project:\/\/database\/Assets\/UI\/Themes\/([a-zA-Z0-9_\-]+)\.tss"\);?/;
const MARKER = '<!-- Theme patched by unity-ui-theme-patch — panel.theme=';
const MARKER_USS = '/* Theme patched by unity-ui-theme-patch — panel.theme=';

function patchUxml(slug, themeSlug) {
  const file = path.join(GENERATED_DIR, `${slug}.uxml`);
  if (!fs.existsSync(file)) return { skipped: 'no-uxml' };
  let text = fs.readFileSync(file, 'utf8');
  const m = text.match(STYLE_SRC_RE);
  if (!m) return { skipped: 'no-Style-src-line' };
  if (m[1] === themeSlug) return { skipped: 'already-applied' };
  const before = text;
  text = text.replace(STYLE_SRC_RE, `<Style src="project://database/Assets/UI/Themes/${themeSlug}.tss"/>`);
  // Insert marker comment immediately above the Style line if absent.
  if (!text.includes(MARKER)) {
    text = text.replace(
      `<Style src="project://database/Assets/UI/Themes/${themeSlug}.tss"/>`,
      `${MARKER}${themeSlug} -->\n  <Style src="project://database/Assets/UI/Themes/${themeSlug}.tss"/>`,
    );
  } else {
    text = text.replace(/<!-- Theme patched by unity-ui-theme-patch — panel\.theme=[a-zA-Z0-9_\-]+ -->/, `${MARKER}${themeSlug} -->`);
  }
  if (text === before) return { skipped: 'no-change' };
  if (!dryRun) fs.writeFileSync(file, text);
  return { patched: file, from: m[1], to: themeSlug };
}

function patchUss(slug, themeSlug) {
  const file = path.join(GENERATED_DIR, `${slug}.uss`);
  if (!fs.existsSync(file)) return { skipped: 'no-uss' };
  let text = fs.readFileSync(file, 'utf8');
  const m = text.match(IMPORT_URL_RE);
  if (!m) return { skipped: 'no-import-url-line' };
  if (m[1] === themeSlug) return { skipped: 'already-applied' };
  const before = text;
  text = text.replace(IMPORT_URL_RE, `@import url("project://database/Assets/UI/Themes/${themeSlug}.tss");`);
  if (!text.includes(MARKER_USS)) {
    text = text.replace(
      `@import url("project://database/Assets/UI/Themes/${themeSlug}.tss");`,
      `${MARKER_USS}${themeSlug} */\n@import url("project://database/Assets/UI/Themes/${themeSlug}.tss");`,
    );
  } else {
    text = text.replace(/\/\* Theme patched by unity-ui-theme-patch — panel\.theme=[a-zA-Z0-9_\-]+ \*\//, `${MARKER_USS}${themeSlug} */`);
  }
  if (text === before) return { skipped: 'no-change' };
  if (!dryRun) fs.writeFileSync(file, text);
  return { patched: file, from: m[1], to: themeSlug };
}

function main() {
  const items = panelsItems();
  const results = [];
  let patched = 0;
  let skipped = 0;
  for (const item of items) {
    const slug = item.slug;
    if (!slug) continue;
    if (slugFilter && !slugFilter.has(slug)) continue;
    const themeSlug = (item.fields && item.fields.theme) || (item.panel && item.panel.theme) || 'dark';
    const ux = patchUxml(slug, themeSlug);
    const us = patchUss(slug, themeSlug);
    results.push({ slug, themeSlug, uxml: ux, uss: us });
    if (ux.patched) patched++;
    else skipped++;
    if (us.patched) patched++;
    else skipped++;
  }
  console.log(JSON.stringify({ dryRun, patched, skipped, results }, null, 2));
}

main();
